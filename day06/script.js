// Loops

// 1. Iterate 0 to 10 using for loop, do the same using while and do while loop
// for loop
for(let i = 0; i <= 10; i++){
    console.log(i);
}

// while loop
let i = 0;
while (i <= 10) {
    console.log(i);
    i++;
}

// do while loop
let j = 0;
do {
    console.log(j);
    j++
} while (j <= 10);


// 2. Iterate 10 to 0 using for loop, do the same using while and do while loop
for(let i = 10; i >= 0; i--){
    console.log(i);
}

// while loop
let k = 10;
while(k >=0 ){
    console.log(k);
    k--;
}

//do while loop
let m = 10;
do{
    console.log(m);
    m--;
} while (m >= 0);